public class DataTypes {
    public static void main(String[] args) {
        ////Start///..............Primitive Data Type..................
        //Byte: whole numbers from range -128 to 127
        byte myByte = 127;
        //Short: whole numbers from -32768 to 32767
        short myShort = 32767;
        //Int -2147483648 to 2147483647
        int myNum = 1;
        //Long : -9223372036854775808 to 9223372036854775807. Note end value with an L.
        long myLong = 9223372036854775807L;
        // Float: Fractional numbers from 3.4e−038 to 3.4e+038. Note end value with an F.
        float myFloat = 4.7f ;
        // Double: Fractional numbers from 1.7e−308 to 1.7e+308. Note that it ends with d.
        double myDouble = 19.99d;
        //The precision of float is only six or seven decimal digits, while double variables have a precision of about 15 digits.
        //Only True and False
        boolean iAmCorrect = true;
        //Only one character
        char letters = 'H';
        ////End///..............Primitive Data Type..................
        ///Special Datatype??
        String firstString =  "My name is Ratul";
        ////Creates new string everytime
        String newObjectString = new String("Test");
//        System.out.println("String " + newObjectString);
        ///Primitive Datatype
        //tutorial
        int arr [];
        arr = new int[2];
        arr[0] = 3;
        arr[1] = 1;
        ///W3 Schools
        int[] myArr = {10, 20, 30, 40};
//        System.out.println(myArr);///Prints Memory Location
//        System.out.println(myArr[0]);
    }
}
